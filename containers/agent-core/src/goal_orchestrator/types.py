"""Type definitions for the Autonomous Goal Engine.

Data model:
  Goal          — top-level persistent entity (stored in Redis HASH)
  TaskNode      — single executable step in the plan
  TaskGraph     — validated DAG of TaskNodes
  GoalState     — lifecycle state machine
  TaskStatus    — per-task execution status
"""

from __future__ import annotations

from datetime import datetime, timezone
from enum import Enum
from uuid import uuid4

from pydantic import BaseModel, Field, field_validator


# ---------------------------------------------------------------------------
# Enumerations
# ---------------------------------------------------------------------------


class GoalState(str, Enum):
    PLANNING = "planning"
    ACTIVE = "active"
    PAUSED = "paused"
    COMPLETED = "completed"
    FAILED = "failed"


class TaskStatus(str, Enum):
    PENDING = "pending"
    RUNNING = "running"
    DONE = "done"
    FAILED = "failed"
    BLOCKED = "blocked"  # Blocked by policy or high-risk guard


class RiskLevel(str, Enum):
    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"
    CRITICAL = "critical"  # Always requires confirmation regardless of autonomy mode


class AutonomyMode(str, Enum):
    """Controls how autonomously the Goal Engine executes tasks.

    ASSIST: Plan generated; no execution without manual approval per task.
    SEMI:   Auto-execute LOW and MEDIUM risk; confirm HIGH and CRITICAL.
    FULL:   Auto-execute LOW, MEDIUM, HIGH; confirm CRITICAL only.

    PolicyEngine always overrides autonomy mode — autonomy never bypasses policy.
    """
    ASSIST = "assist"
    SEMI = "semi"
    FULL = "full"


# ---------------------------------------------------------------------------
# TaskNode
# ---------------------------------------------------------------------------


class TaskNode(BaseModel):
    """A single executable step in a TaskGraph.

    Arguments are fully determined by the PlanGenerator so execution
    can proceed without a secondary LLM call.
    """

    id: str = Field(default_factory=lambda: str(uuid4())[:8])
    description: str
    skill_name: str
    # Pre-determined arguments for the skill (generated by PlanGenerator)
    arguments: dict[str, str] = Field(default_factory=dict)
    required_capability: str = "controlled"
    risk_level: RiskLevel = RiskLevel.LOW
    dependencies: list[str] = Field(default_factory=list)
    status: TaskStatus = TaskStatus.PENDING
    retries: int = 0
    max_retries: int = 2
    output_summary: str = ""
    error: str = ""
    started_at: str | None = None
    completed_at: str | None = None


# ---------------------------------------------------------------------------
# TaskGraph
# ---------------------------------------------------------------------------


class TaskGraph(BaseModel):
    """Directed Acyclic Graph of TaskNodes.

    Invariants enforced on construction:
    - All dependency IDs must reference valid node IDs
    - No cycles (Kahn's algorithm)
    """

    nodes: list[TaskNode] = Field(default_factory=list)

    @property
    def total_tasks(self) -> int:
        return len(self.nodes)

    def get_node(self, task_id: str) -> TaskNode | None:
        for node in self.nodes:
            if node.id == task_id:
                return node
        return None

    def validate_dag(self) -> tuple[bool, str]:
        """Validate DAG using topological sort (Kahn's algorithm).

        Returns (is_valid, error_message).
        """
        node_ids = {n.id for n in self.nodes}

        # Check all dependency references are valid
        for node in self.nodes:
            for dep_id in node.dependencies:
                if dep_id not in node_ids:
                    return (
                        False,
                        f"Task '{node.id}' references unknown dependency '{dep_id}'",
                    )

        # Kahn's algorithm: topological sort to detect cycles
        in_degree: dict[str, int] = {n.id: 0 for n in self.nodes}
        adj: dict[str, list[str]] = {n.id: [] for n in self.nodes}

        for node in self.nodes:
            for dep_id in node.dependencies:
                adj[dep_id].append(node.id)
                in_degree[node.id] += 1

        queue = [nid for nid, deg in in_degree.items() if deg == 0]
        visited = 0

        while queue:
            nid = queue.pop(0)
            visited += 1
            for child_id in adj[nid]:
                in_degree[child_id] -= 1
                if in_degree[child_id] == 0:
                    queue.append(child_id)

        if visited != len(self.nodes):
            return False, "Cycle detected in task dependency graph"

        return True, ""

    def get_executable_tasks(self) -> list[TaskNode]:
        """Return tasks ready to execute: status=PENDING, all deps DONE."""
        done_ids = {n.id for n in self.nodes if n.status == TaskStatus.DONE}
        return [
            n
            for n in self.nodes
            if n.status == TaskStatus.PENDING
            and all(dep_id in done_ids for dep_id in n.dependencies)
        ]

    def get_next_task(self) -> TaskNode | None:
        """Return the first executable task (depth-first ordering)."""
        executable = self.get_executable_tasks()
        return executable[0] if executable else None

    def compute_progress(self) -> float:
        """Progress 0–100 based on done tasks / total tasks."""
        if not self.nodes:
            return 0.0
        done = sum(1 for n in self.nodes if n.status == TaskStatus.DONE)
        return round((done / len(self.nodes)) * 100, 1)

    def is_complete(self) -> bool:
        return bool(self.nodes) and all(
            n.status == TaskStatus.DONE for n in self.nodes
        )

    def has_permanently_failed(self) -> bool:
        """True if any task failed and exhausted all retries."""
        return any(
            n.status == TaskStatus.FAILED and n.retries >= n.max_retries
            for n in self.nodes
        )

    def get_pending_tasks(self) -> list[TaskNode]:
        return [n for n in self.nodes if n.status == TaskStatus.PENDING]

    def get_failed_tasks(self) -> list[TaskNode]:
        return [
            n
            for n in self.nodes
            if n.status == TaskStatus.FAILED and n.retries >= n.max_retries
        ]


