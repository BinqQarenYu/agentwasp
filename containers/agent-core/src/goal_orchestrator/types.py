class CognitiveBudget(BaseModel):
    """Per-goal spending limits and usage tracking.

    All limits are configurable at goal creation time with system-level
    defaults from config.  Once budget_exceeded is True the goal is
    paused automatically and requires manual resume.
    """

    # Hard limits (set at creation; configurable per goal)
    max_tokens_planning: int = 2000       # Total LLM tokens across all planning calls
    max_tokens_execution: int = 10000     # Reserved; currently tracked post-hoc
    max_replans: int = 5                  # Loosened from 3 — see stability.py rationale
    max_steps: int = 50                   # Mirrors goal.max_steps for budget tracking
    max_parallel_tasks: int = 1           # One task per tick (enforced by scheduler)
    max_memory_growth_bytes: int = 1_048_576  # 1 MB cumulative episodic writes
    max_runtime_seconds: int = 3600       # Mirrors goal.max_runtime_seconds

    # Tracked usage (updated by executor / orchestrator)
    tokens_used_planning: int = 0
    tokens_used_execution: int = 0
    replans_used: int = 0
    steps_executed: int = 0
    memory_growth: int = 0               # Cumulative bytes written to episodic memory

    # Violation state
    budget_exceeded: bool = False
    budget_exceeded_dimension: str = ""  # e.g. "tokens_planning", "replans", "steps"


class StabilityState(BaseModel):
    """Runtime stability tracking for intervention decisions.

    Enforces:
    - Exponential backoff on 3 consecutive task failures
    - Pause on 4 replans within 5 minutes (replan storm)
    - Lock on oscillating failure pattern
    - Block repeated policy violations on same capability
    """

    consecutive_failures: int = 0
    backoff_until: str | None = None          # ISO datetime; None = no backoff
    in_backoff: bool = False
    replan_timestamps: list[str] = Field(default_factory=list)  # ISO datetimes
    locked: bool = False                      # True if oscillation detected
    policy_block_counts: dict[str, int] = Field(default_factory=dict)
    last_intervention: str = ""               # ISO datetime of last intervention
    intervention_reason: str = ""


class GoalTelemetry(BaseModel):
    """Lightweight per-goal telemetry for observability.

    Tracks skill distribution, policy blocks, autonomy decisions, etc.
    Serialized as part of Goal in Redis HASH — intentionally compact.
    """

    skill_distribution: dict[str, int] = Field(default_factory=dict)
    policy_blocks: int = 0
    budget_exceeded_events: int = 0
    stability_interventions: int = 0
    autonomy_decisions: dict[str, int] = Field(default_factory=dict)  # "auto"/"blocked" → count
    template_id: str | None = None
    template_name: str = ""


