class GoalEvent:
    """Lightweight observability event — emitted at every state transition."""

    __slots__ = (
        "event_name",
        "goal_id",
        "task_id",
        "correlation_id",
        "project_container_id",
        "chat_id",
        "latency_ms",
        "model_used",
        "detail",
    )

    def __init__(
        self,
        event_name: str,
        goal_id: str,
        task_id: str = "",
        correlation_id: str | None = None,
        project_container_id: str = "",
        chat_id: str = "",
        latency_ms: int = 0,
        model_used: str = "",
        detail: str = "",
    ):
        self.event_name = event_name
        self.goal_id = goal_id
        self.task_id = task_id
        self.correlation_id = correlation_id or str(uuid4())
        self.project_container_id = project_container_id
        self.chat_id = chat_id
        self.latency_ms = latency_ms
        self.model_used = model_used
        self.detail = detail
