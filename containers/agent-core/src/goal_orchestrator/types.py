# Goal
# ---------------------------------------------------------------------------


class Goal(BaseModel):
    """Persistent goal entity — stored as JSON in Redis HASH 'goals'."""

    id: str = Field(default_factory=lambda: str(uuid4()))
    project_container_id: str = ""
    objective: str
    title: str = ""   # Short display title (auto-generated from objective)
    constraints: str = ""
    success_criteria: str = ""

    # Lifecycle
    state: GoalState = GoalState.PLANNING
    task_graph: TaskGraph = Field(default_factory=TaskGraph)
    progress: float = 0.0
    replan_count: int = 0
    step_count: int = 0
    # Plan lock: set True after first task success; prevents replanning on still-succeeding plans
    plan_locked: bool = False

    # Safety limits
    max_steps: int = 50
    max_runtime_seconds: int = 3600  # 1 hour

    # Timestamps
    created_at: str = Field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )
    updated_at: str = Field(
        default_factory=lambda: datetime.now(timezone.utc).isoformat()
    )
    started_at: str | None = None
    completed_at: str | None = None

    # Origin
    chat_id: str = ""
    user_id: str = ""
    error: str = ""

    # Advanced Control Layer (Phase 17+)
    autonomy_mode: AutonomyMode = AutonomyMode.SEMI     # Default: confirm HIGH tasks
    budget: CognitiveBudget = Field(default_factory=CognitiveBudget)
    stability: StabilityState = Field(default_factory=StabilityState)
    telemetry: GoalTelemetry = Field(default_factory=GoalTelemetry)

    # Multi-Agent Orchestration Layer (Phase 18+)
    # allowed_capabilities: list of allowed capability levels for this goal.
    # Empty = unrestricted (inherits global CapabilityRegistry settings).
    # Set by AgentRuntime.create_goal() from agent.allowed_capabilities.
    allowed_capabilities: list[str] = Field(default_factory=list)
    # agent_id: owning agent (empty = standalone goal, not agent-managed)
    agent_id: str = ""

    # Priority Arbitration (1=lowest, 10=highest).
    # User-initiated goals default to 8; autonomous/agent goals default to 3.
    # tick() processes goals ordered highest-priority-first so user work is
    # never starved by background autonomous activity.
    priority: int = Field(default=5, ge=1, le=10)
    # Source: who initiated this goal ("user", "dashboard", "autonomous", "agent")
    source: str = "user"

    @field_validator("priority", mode="before")
    @classmethod
    def _clamp_priority(cls, v: int) -> int:
        return max(1, min(10, int(v)))

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def touch(self) -> None:
        """Update the updated_at timestamp."""
        self.updated_at = datetime.now(timezone.utc).isoformat()

    def runtime_seconds(self) -> float:
        """Elapsed runtime since goal became ACTIVE."""
        if not self.started_at:
            return 0.0
        try:
            start = datetime.fromisoformat(self.started_at)
            now = datetime.now(timezone.utc)
            return (now - start).total_seconds()
        except Exception:
            return 0.0

    def is_runtime_exceeded(self) -> bool:
        return self.runtime_seconds() > self.max_runtime_seconds

    def is_steps_exceeded(self) -> bool:
        return self.step_count >= self.max_steps


